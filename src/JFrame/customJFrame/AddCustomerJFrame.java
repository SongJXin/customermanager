    private void addCustomer_OK_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomer_OK_ButtonActionPerformed
        // TODO add your handling code here:
        Customer customer=new Customer();
        customer.setC_Address(addCustomer_Address_TextField.getText());
        customer.setC_Card_Number(addCustomer_CardNumber_TextField.getText());
        customer.setC_Cash(Float.valueOf(addCustomer_Cash_TextField.getText()));
        customer.setC_Integration(Float.valueOf(addCustomer_integration_TextField.getText()));
        customer.setC_Name(addCustomer_Name_TextField.getText());
        customer.setC_Remark(addCustomer_Remark_TextArea.getText());
        customer.setC_Sex(String.valueOf(addCustomer_Sex_ComboBox.getSelectedItem()));
        customer.setC_Tell(addCustomer_TELL_TextField.getText());
        new CustomerDao().add(customer);
        dispose();
    }//GEN-LAST:event_addCustomer_OK_ButtonActionPerformed

