    private void findGoods_OKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findGoods_OKButtonActionPerformed
        // TODO add your handling code here:
       //获取table的model
        DefaultTableModel tableModel = (DefaultTableModel)jTable.getModel();
        //声明list
        ArrayList list=new ArrayList();
        //声明map
        Map rowDataMap=new HashMap();
        //声明dao
        GoodsDao goodsDao=new GoodsDao();
        //获取对话框内容
        String nameString=g_find_Name_TextField.getText();
        String brandString=g_find_Brand_TextField.getText();
        String pricingwayString=g_updata_PricingWay_ComboBox.getSelectedItem().toString();
        String numbigString=g_find_Inventorymorethan_TextField1.getText();
        String numsmallString=g_find_Inventorylessthan_TextField.getText();
        String pricebigString=g_find_PriceBiggerthan_TextField.getText();
        String pricesmallString=g_find_PriceSmallerthan_TextField1.getText();
        //查询
        list=goodsDao.list(nameString, brandString, pricingwayString, numbigString, numsmallString, pricebigString, pricesmallString);
        //没有查到结果时报错
        if(list==null){
             JOptionPane.showMessageDialog(null, "没有找到查询结果", "查询失败", JOptionPane.ERROR_MESSAGE); 
        }else{
            //计数器，记录第几行
            int i=0;
            //迭代器
            Iterator it1 = list.iterator();
            //重置table内容，避免第二次查询结果少于第一次时，第一次的内容依然存在
            tableModel.setRowCount(list.size());
            //遍历
            while(it1.hasNext()){
                rowDataMap=(Map) it1.next();
                //显示
                tableModel.setValueAt(rowDataMap.get("g_id"), i,0);
                tableModel.setValueAt(rowDataMap.get("g_name"), i, 1);
                tableModel.setValueAt(rowDataMap.get("g_brand"), i, 2);
                tableModel.setValueAt(rowDataMap.get("g_price"), i, 3);
                tableModel.setValueAt(rowDataMap.get("g_inventory"), i, 4);
                tableModel.setValueAt(rowDataMap.get("g_pricingway"), i, 5);
                tableModel.setValueAt(rowDataMap.get("g_remark"), i, 6);
                
                i++;
            } 
            //显示条目数量
            jLabel.setText(String.valueOf(i));
        }
        
     //关闭
        dispose();
    }//GEN-LAST:event_findGoods_OKButtonActionPerformed
